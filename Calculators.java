package week1.day2.homeAssignment;

public class Calculators {
 // creating methods

	public int performAdd(int a,int b,int c) {
		int sum = a + b + c;
		return sum;
	}

	public int performSubtract(int x,int y){
		int sub = x-y;
		return sub;
	}

	public double performMul(double m,double n){
		double mul = m*n;
		return mul;
	}

	public float performDivide(float e,float f) {
		float divide = e/f;
		return divide;
	}

}



